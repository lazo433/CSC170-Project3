                    <h2> Release of <em> Life is Strange </em> </h2>
                    <p> The Pacific Northwest was chosen for the setting to convey a nostalgic and autumnal feel, with the team eventually settling on Oregon as the location of the fictional town of Arcadia Bay. It was decided early on that most of the budget be spent on the writing and voice actors. The story was originally written in French by Jean-Luc Cano, and converted into a game script by the game's co-directors and design team. This was then handed over to Christian Divine and Cano to be finetuned in English. Recording sessions were done in Los Angeles, California, with the French developer brought in via Skype. The textures were hand painted, adapted to achieve what art director Michel Koch called "impressionistic rendering". Sources of inspiration include the visual novel Danganronpa, in terms of balancing gameplay and story, and the novel The Catcher in the Rye, whose protagonist Holden Caulfield shares a surname with Max, the lead character. For the sake of realism, the supernatural elements were designed as a metaphor for the characters' inner conflict,  and experts were consulted to tackle difficult subjects such as teen suicide.
                        Following the release and success of the first <em> Life is Strange </em>, publisher Square Enix chose American developer Deck Nine to develop a prequel game focusing on the life of Chloe Price, while the Dontnod team began developing a direct sequel. Development on the project began in 2016 with assistance from Square Enix' London Studios. Ashly Burch, who voiced Chloe in <em> Life Is Strange </em>, was replaced by Rhianna DeVries due to the SAG-AFTRA strike. However, Burch and Hannah Telle, who voiced Max Caulfield, both reprised their roles for the bonus episode Farewell. The project went under various working titles during development. The writers researched memoirs and psychology to understand Chloe's grieving process, and the script for the game was over 1,500 pages, written by lead writer Zak Garriss and a writers' room. Prior to its official announcement, images had leaked online indicating that a prequel to <em> Life Is Strange </em> was in development. Square Enix revealed <em> Life Is Strange: Before the Storm </em> on 11 June during Microsoft's E3 2017 Presentation.
                    </p>
