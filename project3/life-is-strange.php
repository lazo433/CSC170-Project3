                <h2>Episodes</h2>

                <h3> <strong> Episode 1: Chrysalis </strong> </h3>

		            <p> The first chapter of the game introduces the player to Max Caulfield, an 18 year old college student. She’s nervous about returning to Arcadia Bay for many reasons, but the main one being her best friend: Chloe Price. When Max and her parents moved to Seattle in 2013, it happened right after the death of her best friend’s dad. Max feels guilty for abandoning Chloe during that time, so she’s anxious about their inevitable reunion. In this chapter, after witnessing the murder of Chloe Price in the school’s bathroom, Max also discovers her ability to rewind time and begins to have visions of a massive tornado destroying Arcadia bay the upcoming Friday. Additionally, we are introduced to the main mystery of Arcadia bay: Rachel Amber. A missing girl and also Chloe’s best friend in Max’s absence. Using her newfound power, she saves Chloe, but doesn’t realize it’ll come with a cost. </p>

	            <h3> Episode 2: Out of Time </h3>

		            <p> After revealing her power to Chloe, the pair of reconciled friends take Max’s rewind ability to the test. In a series of min-games and events, Max tests out her powers and pushes the limits of her abilities. Meanwhile, one of Max’s friends, Kate Marsh, is suffering from depression after a viral video of her at a party is the talk of the school. Throughout the chapter, we see Kate’s well-being deteriorate and eventually culminate in an attempt to take her life. Using her powers, Max stops time completely and manages to make it to Kate to try and save her. Based on the player’s choice throughout the chapter, Kate’s life can either be saved or lost. </p>

	            <h3> Episode 3: Chaos Theory </h3>

		            <p> After the traumatic events that transpired in chapter 2, Chloe tries to comfort Max. However, time is pressing and they still want to find out what happened to Rachel Amber. Their investigation leads them to some unpleasant truths that cause Chloe to spiral. Her and Max get into an argument and when Max returns to her dorm, she reminisces over a picture of her and Chloe as kids. Unintentionally, she discovers a new use of her power and she travels to the time when the picture was taken. She is transported to the day of Chloe’s dad’s death and after changing the course of events, she’s able to save the life of her dad. The changes to her current timeline are completely altered and when she returns to the current day, she finds that Chloe’s father is alive, but Chloe is now completely paralyzed from a car accident she experienced. </p>

	            <h3> Episode 4: Dark Room </h3>

		            <p> After being transported to this alternate reality with a disabled Chloe, Max begins to understand the weight of her powers and tries to find a way back to her original reality. This means returning to the day Chloe’s dad died and letting fate run its course without interference. Back in her reality, Max and Chloe continue their search for Rachel Amber. They are led to an underground bunker where they find evidence of someone drugging young women at parties, then photographing them for sinister purposes. When it happened to Rachel Amber, she was accidentally given a drug too strong and she ended up dying. This revelation breaks Chloe and she becomes hellbent on revenge. They are baited to Rachel’s resting place, where it’s revealed that the academy’s professor is behind everything, but he manages to kill Chloe and drug Max, marking her as his next victim. </p>

	            <h3>  Episode 5: Polarized  </h3>

		            <p> In the final chapter, the storm in Max’s visions finally arrives, but she’s trapped in Mark Jefferson’s bunker. Using her powers, she manages to escape and witness the destruction occurring in Arcadia Bay. After encountering many characters you’ve met throughout the game and living a nightmare sequence brought on by the overuse of her powers, Max must make a final choice. It’s revealed that the reason for the storm started at the beginning of the game, with Max interfering with fate by saving Chloe’s life. In the end, the player must make a choice of sacrificing Arcadia Bay and all of its residents or sacrificing Chloe in order to avoid the storm. This choice is a culmination of all the precious moments spent with Chloe and most players have a hard time choosing who to save. </p>

