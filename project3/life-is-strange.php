                            <p> Following the critical and commercial failure of their 2013 action-adventure game <em> Remember Me </em>, French game developer Dontnod Entertainment was forced to restructure and reinvent themselves. Dontnod CEO Oskar Guilbert explained "Before, what we wanted to do was make big games with large teams and long production cycles... So we had to change that, do something smaller with smaller teams. We didn’t want to do just a small game; we also wanted to bring something new to the market."
                                <em> Life Is Strange </em> was conceived around the rewind mechanic, a concept which had already been briefly explored in <em> Remember Me </em>. <em> Life Is Strange's </em> protagonist Max Caulfield was created with the ability to rewind time to supplement this mechanism. Development began in April 2013 with a team of 15 people, with further people added as the collaboration with Square Enix began. Before signing with Square Enix, Dontnod imagined <em> Life Is Strange </em> as a full-length video game similar to <em> Remember Me </em> that Dontnod would self-publish; however, Square Enix realized it would be more successful as an episodic title, both for creative reasons, financial restrictions and marketing purposes. This allowed them to tell the story at a slower pace.
