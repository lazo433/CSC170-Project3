                        <h3>The First Cup</h3>
                        <p>The first <strong>European Cup</strong> took place during the <strong>1955&ndash;56 season</strong>. <strong>Sixteen teams</strong> participated (some by invitation): <strong>AC Milan</strong> (Italy), <strong>AGF Aarhus</strong> (Denmark), <strong>Anderlecht</strong> (Belgium), <strong>Djurgården</strong> (Sweden), <strong>Gwardia Warszawa</strong> (Poland), <strong>Hibernian</strong> (Scotland), <strong>Partizan</strong> (Yugoslavia), <strong>PSV Eindhoven</strong> (Netherlands), <strong>Rapid Wien</strong> (Austria), <strong>Real Madrid</strong> (Spain), <strong>Rot-Weiss Essen</strong> (West Germany), <strong>Saarbrücken</strong> (Saar), <strong>Servette</strong> (Switzerland), <strong>Sporting CP</strong> (Portugal), <strong>Reims</strong> (France), and <strong>Vörös Lobogó</strong> (Hungary).</p>
                        <p>The first <strong>European Cup match</strong> took place on <strong>4 September 1955</strong>, and ended in a <strong>3&ndash;3 draw</strong> between <strong>Sporting CP</strong> and <strong>Partizan</strong>. The first goal in European Cup history was scored by <strong>João Baptista Martins</strong> of <strong>Sporting CP</strong>. The inaugural final took place at the <em>Parc des Princes</em> between <strong>Stade de Reims</strong> and <strong>Real Madrid</strong> on <strong>13 June 1956</strong>. The Spanish squad came back from behind to win <strong>4&ndash;3</strong> thanks to goals from <strong>Alfredo Di Stéfano</strong> and <strong>Marquitos</strong>, as well as two goals from <strong>Héctor Rial</strong>. <strong>Real Madrid</strong> successfully defended the trophy against <strong>Fiorentina</strong> next season in their home stadium, the <em>Santiago Bernabéu</em>. After a scoreless first half, <strong>Real Madrid</strong> scored twice in six minutes to defeat the Italians. In <strong>1958</strong>, <strong>Milan</strong> failed to capitalize after going ahead on the scoreline twice, only for <strong>Real Madrid</strong> to equalize. The final, held in <em>Heysel Stadium</em>, went to extra time where <strong>Francisco Gento</strong> scored the game-winning goal to allow <strong>Real Madrid</strong> to retain the title for the third consecutive season.</p>
                    </section>
                    <section>
                        <h3>1967&ndash;68 season</h3>
                        <p>The 1967&ndash;68 season saw Manchester United become the first English team to win the European Cup, beating two-time winners Benfica 4&ndash;1 in the final. This final came ten years after the Munich air disaster, which had claimed the lives of eight United players and left their manager, Matt Busby, fighting for his life. In the 1968&ndash;69 season, Ajax became the first Dutch team to reach the European Cup final, but they were beaten 4&ndash;1 by Milan, who claimed their second European Cup, with Pierino Prati scoring a hat-trick.</p>
                    </section>
                    <section>
                        <h3>1969&ndash;70 season</h3>
                        <p>The 1969&ndash;70 season saw the first Dutch winners of the competition. Feyenoord knocked out the defending champions, Milan in the second round, before beating Celtic in the final. In the 1970&ndash;71 season, Ajax won the title, beating Greek side Panathinaikos in the final.] the season saw a number of changes, with penalty shoot-outs being introduced, and the away goals rule being changed so that it would be used in all rounds except the final. It was also the first time a Greek team reached the final, as well as the first season that Real Madrid failed to qualify, having finished sixth in La Liga the previous season. Ajax went on to win the competition three years in a row (1971 to 1973), which Bayern Munich emulated from 1974 to 1976 before Liverpool won their first two titles in 1977 and 1978.</p>
                    </section>
