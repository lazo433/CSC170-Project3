                    <h2>Format</h2>
                    <p>The UEFA Champions League format is structured into multiple stages, allowing top football clubs from Europe to compete for the title. Here's is how the format looks:</p>
    
                    <dl class="side-lists">
                        <dt><strong>Qualification Rounds</strong></dt>
                        <dd>- Teams from lower-ranked European leagues must compete in several preliminary and qualifying rounds. The number of qualifying rounds depends on their league's UEFA coefficient ranking</dd>
                        
                        <dt><strong>Group Stage</strong></dt>
                            <dd>- 32 teams participate, divided into 8 groups of 4</dd>
                            <dd>- Each team plays 6 matches (home and away) against the other teams in their group</dd>
                            <dd>- The top 2 teams from each group (16 total) advance to the knockout stage</dd>
                            <dd>- The 3rd-placed team from each group moves to the UEFA Europa League knockout phase</dd>
    
                        <dt><strong>knockout Stage</strong></dt>
                            <dd>- The 16 advancing teams play two-legged ties (home and away) in the Round of 16, quarterfinals, and semifinals</dd>
                            <dd>- The team with the highest aggregate score over the two matches moves on to the next round. If scores are tied, extra time and penalties may be used to determine the winner</dd>
                        <dt><strong>Final</strong></dt>
                            <dd>- A single match is played at a neutral venue to decide the champion</dd>
                    </dl>
    
                    <p>The tournament typically runs from September to May. The winning team qualifies for the UEFA Super Cup and the FIFA Club World Cup</p>
                    
