                        <h2>History</h2> 
                        <img src="./images/cl-patch.jpg" alt="Showing 4 images of the champions league patch of the years" style="width: 250px;">              
                        <p>The first time the champions of two European leagues met was in what was nicknamed the <em>1895 World Championship</em> when <strong>English champions Sunderland</strong> beat <strong>Scottish champions Heart of Midlothian</strong> 5&ndash;3. The first pan-European tournament was the <em>Challenge Cup</em>, a competition between clubs in the Austro-Hungarian Empire. Three years later, in <strong>1900</strong>, the champions of <strong>Belgium</strong>, <strong>Netherlands</strong>, and <strong>Switzerland</strong>, which were the only existing leagues in continental Europe at the time, participated in the <em>Coupe Van der Straeten Ponthoz</em>, thus being dubbed the <em>"club championship of the continent"</em> by the local newspapers.</p>
                    </header>
