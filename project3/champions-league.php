                    <h2>Champions League Anthem</h2>
                    <p>The <strong>UEFA Champions League anthem</strong>, officially titled simply as <em>"Champions League"</em>, was written by <strong>Tony Britten</strong>, and is an adaptation of <em>George Frideric Handel's 1727 anthem Zadok the Priest</em> (one of his Coronation Anthems). UEFA commissioned Britten in <strong>1992</strong> to arrange an anthem, and the piece was performed by <em>London's Royal Philharmonic Orchestra</em> and sung by the <em>Academy of St. Martin in the Fields</em>. Stating that <em>"the anthem is now almost as iconic as the trophy"</em>, UEFA's official website adds it is <em>"known to set the hearts of many of the world's top footballers aflutter."</em></p>
